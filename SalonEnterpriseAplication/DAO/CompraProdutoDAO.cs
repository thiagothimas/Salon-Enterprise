using SalonEnterpriseAplication.Models;
using System;
using System.Collections.Generic;
using System.Linq;
using System.Web;

namespace SalonEnterpriseAplication.DAO
{
    public class CompraProdutoDAO
    {
        public IList<CompraProduto> Lista()
        {
            using (var contexto = new SalonEnterpriseContext())
            {
                return contexto.tb_compra_produto.ToList();
            }
        }

        public void Adiciona(CompraProduto compraProduto)
        {
            using (var context = new SalonEnterpriseContext())
            {
                context.tb_compra_produto.Add(compraProduto);
                context.SaveChanges();
            }
        }
    }
}